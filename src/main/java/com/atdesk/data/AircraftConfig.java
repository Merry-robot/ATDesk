package com.atdesk.data;

public class AircraftConfig {
    private String id;
    private String callsign;
    private String state;
    private double x;
    private double y;
    private String runway;

    public String getId() {
        return id;
    }

    public String getCallsign() {
        return callsign;
    }

    public String getState() {
        return state;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public String getRunway() {
        return runway;
    }
}
